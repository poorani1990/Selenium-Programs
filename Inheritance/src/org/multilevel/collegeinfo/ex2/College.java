//	Grand Parent Class

package org.multilevel.collegeinfo.ex2;

public class College {

	public void collegeName() {
		System.out.println("College Information: ");
		System.out.println("College Name: XYZ");

	}
	
	public void collegeCode() {
		System.out.println("College code: 215");
	}

	public void collegeRank() {
		System.out.println("College Rank: 15");
	}

}

//	Main method is not required when extending parent class to child class
