package org.com;

public class IfElsestatement {

	public static void main(String[] args) {
		
		int age = 8;
		//simple if
		
		if(age>=18)
			// if age is not >= 18 then nothing will be printed
		{
			System.out.println("You are eligible to vote");
		}
		else {
			System.out.println("You are not eligible to vote");
		}
	}
}
